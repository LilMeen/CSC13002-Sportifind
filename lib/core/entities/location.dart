class Location {
  final String name;
  final String address;
  final String district;
  final String city;
  final double latitude;
  final double longitude;
  final String fullAddress;

  Location({
    this.name = '',
    this.address = '',
    this.district = '',
    this.city = '',
    this.latitude = 0,
    this.longitude = 0,
    this.fullAddress = '',
  });
}